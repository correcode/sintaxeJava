public class PlanoOperadora {
    public static void main(String[] args) {
        String plano = "Turbo";

        switch (plano) {
            case "Turbo": {
                System.out.println("\n5G Youtube");
            }
            case "Midium": {
                System.out.println("\nWhatsapp Ilimitado");
            }
            case "Minimum": {
                System.out.println("\n10G de Franquia total");
            }
        }
    }
}
